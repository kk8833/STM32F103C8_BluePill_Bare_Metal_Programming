void OLED_char(uint8_t word)
{
	if (word>='a' && word<='z')
	{
		word=word-32;
	}
	if (word<32 || word>90)
	{
		word= ' ';
	}

	uint8_t font_index= word-32;
	I2C_start();
	I2C_send_addr(0x3C);
	I2C_write_byte(0x40);
	for (uint8_t col=0; col<5; col++)
	{
	    while (!(I2C_SR1 & (1 << 7)));
	    I2C_DR= Font5x7[font_index][col];
	}

	while (!(I2C_SR1 & (1 << 7)));
	    I2C_DR = 0x00;

	    I2C_stop();
}
void OLED_str(char *str)
{
	while(*str)
	{

		OLED_char(*str++);
	}
}

